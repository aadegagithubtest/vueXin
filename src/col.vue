<template>
<div class="col" :class="[colClasses]"
     :style="colStyle"
>
        <slot>
        </slot>
</div>
</template>

<script>
    let validator = val=>{
        let keys = Object.keys(val)
        let valid = true
        keys.forEach(key=>{
            if(!['span','offset'].includes(key)){
                valid = false
            }
        })
        return valid
    }
    export default {
        name: "gCol",
        props:{
            span:{
                type:[Number,String]
            },
            offset:{
                type:[Number,String]
            },
            pad:{type:Object, validator},
            np:{type:Object, validator},
            pc:{type:Object, validator},
            wp:{type:Object, validator},
        },
        data(){
            return {
                gutter:0,
            }
        },
        computed:{
            colClasses(){
                let {span,offset,pad,np,pc,wp} = this
                return [
                    span && `col-${span}`,
                    offset && `offset-${offset}`,
                    ...[pad && `col-iPad-${pad.span}`],
                    ...[np && `col-narrowPc-${np.span}`],
                    ...[pc && `col-pc-${pc.span}`],
                    ...[wp && `col-widePc-${wp.span}`],
            ]
            },
            colStyle(){
                return {
                    paddingLeft:this.gutter/2 + 'px',
                    paddingRight:this.gutter/2 + 'px'
                }
            }
        }


    }
</script>

